# 练习：
# 　输入一个字符串：
# 　１、判断您输入的字符有几个空格
# 　２、将原字符串的左右空白字符去掉，打印出有效的字符个数
# 　３、判断您输入的是否是数字，
# 　　　如果是数字，判断用户输入的数字是否大于１００


s = input("请输入一个字符串: ")
s1 = s.count(" ")
print("您输入的字符串有", s1,"个空格")
s2 = s.strip()
print("去掉空白字符后", s2)
print("有效字符的个数", len(s2))
if s2.isdigit():
    print("您刚才输入的是数字")
    n = int(s2)
    if n > 100:
        print("您刚才输入的大于100")
    elif n < 100:
        print("您刚输入的小于100")
    else:
        print("您输入的是100")
else:
    print("您输入的不是数字")
